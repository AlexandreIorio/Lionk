namespace Lionk.Utils;

/// <summary>
///     Represents the type of the configuration file.
/// </summary>
public enum FolderType
{
    /// <summary>
    ///     Represents the configuration folder.
    /// </summary>
    Config,

    /// <summary>
    ///     Represents the log folder.
    /// </summary>
    Logs,

    /// <summary>
    ///     Represents the data folder.
    /// </summary>
    Data,

    /// <summary>
    ///     Represents the plugin folder.
    /// </summary>
    Plugin,

    /// <summary>
    ///     Represents the temporary folder.
    /// </summary>
    Temp,
}
